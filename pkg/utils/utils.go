// THIS WILL HELP RETURN UNMARSHEL DATA TO USE IN CONTROLLER FILE
//UNMARSHEL DATA = JSON---->BYTE

package utils

import (
	"encoding/json"
	"io/ioutil"
	"net/http"
)

func ParseBody(r *http.Request, x interface{}) {
	if body, err := ioutil.ReadAll(r.Body); err == nil {
		if err := json.Unmarshal([]byte(body), x); err != nil {
			return
		}
	}
}
